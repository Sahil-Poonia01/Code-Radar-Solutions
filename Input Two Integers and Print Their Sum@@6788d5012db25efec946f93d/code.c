#include <stdio.h>

int main() {
    int num1, num2, sum; // Variables to store the two integers and their sum

 
    // Taking two integer inputs using scanf
    scanf("%d %d", &num1, &num2);

    // Calculating the sum of the two integers
    sum = num1 + num2;

    // Printing the sum using printf
    printf("Sum: %d\n", sum);

    return 0;
}
